package co.edu.sena.Miscelanea;

public class Ejercicios 
{
    public double areaTriangulo(double d1, double d2){
        return (d1*d2) / 2;
    }
    public double SumaDeDosNum(double d1, double d2){
        return d1+d2;
    }
    public double NumeroAlCuadrado(double d1){
        return d1*d1;
    }
    public double EuroADolar(double d1){
        return d1*1.08;
    }
    public double AreaYPerimetroCuadrado(double d1){
        return d1 * d1;
        return d1*4;
    }
    public double AreaYVolumenCilindro(double d1, double d2){
        return 2*3.14*d1*(d1 + d2);
        return (3.14 * (d1 * d1)) * d2;
    }
    public double AreaYLongitudCirc(double d1){
        return 3.14 * ( d1 * d1 );
        return 2*3.14*d1;
    }
    public double PromedioTresNumeros(double d1, double d2, double d3){
        return (d1+d2+d3)/3;
    }
}